#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");
process.chdir(root);

const localCloudDir = path.join(root, "cloud");
const packageCloudDir = path.join(root, "node_modules", "@decernhq", "cloud");
const generatedMarker = "// GENERATED BY scripts/vercel-prebuild.mjs – do not edit manually";
const legacyMarker = "// GENERATED BY cloud/setup.sh";

function walkRouteFiles(dir) {
  if (!fs.existsSync(dir)) return [];
  const entries = fs.readdirSync(dir, { withFileTypes: true });
  const files = [];
  for (const entry of entries) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      files.push(...walkRouteFiles(full));
      continue;
    }
    if (entry.isFile() && entry.name === "route.ts") {
      files.push(full);
    }
  }
  return files;
}

function cleanGeneratedApiProxies() {
  const apiRoot = path.join(root, "app", "api");
  if (!fs.existsSync(apiRoot)) return;

  const allRoutes = walkRouteFiles(apiRoot);
  for (const routeFile of allRoutes) {
    const content = fs.readFileSync(routeFile, "utf8");
    if (!content.includes(generatedMarker) && !content.includes(legacyMarker)) continue;
    fs.rmSync(routeFile, { force: true });
  }
}

function ensureCloudApiProxies(sourceApiDir) {
  const routeFiles = walkRouteFiles(sourceApiDir);
  if (routeFiles.length === 0) {
    console.log("[vercel-prebuild] No cloud API routes found in source.");
    return;
  }

  console.log("[vercel-prebuild] Creating cloud API proxy route files...");
  for (const src of routeFiles) {
    const relativeFromSourceRoot = path.relative(sourceApiDir, src);
    const targetProxy = path.join(root, "app", "api", relativeFromSourceRoot);
    const targetDir = path.dirname(targetProxy);
    fs.mkdirSync(targetDir, { recursive: true });

    const importTargetNoExt = src.replace(/\.ts$/, "");
    let relImport = path.relative(targetDir, importTargetNoExt).replace(/\\/g, "/");
    if (!relImport.startsWith(".")) relImport = `./${relImport}`;

    fs.writeFileSync(
      targetProxy,
      `${generatedMarker}\nexport * from "${relImport}";\n`,
      "utf8"
    );
    console.log(`  ${path.relative(root, targetProxy)} -> ${relImport}`);
  }
  console.log("[vercel-prebuild] Done. Cloud API routes are now active.");
}

if (fs.existsSync(path.join(root, "protocol", "src"))) {
  console.log("[vercel-prebuild] Building local protocol/...");
  const { execSync } = await import("node:child_process");
  try {
    execSync("npm run build", { cwd: path.join(root, "protocol"), stdio: "inherit" });
    console.log("[vercel-prebuild] Protocol build complete.");
  } catch (e) {
    console.error("[vercel-prebuild] Protocol build failed:", e.message);
    process.exit(1);
  }
} else {
  console.log("[vercel-prebuild] No local protocol/ directory. Using @decern/protocol from npm.");
}

cleanGeneratedApiProxies();

if (fs.existsSync(path.join(localCloudDir, "app", "api"))) {
  console.log("[vercel-prebuild] Using local cloud/ + route proxies...");
  ensureCloudApiProxies(path.join(localCloudDir, "app", "api"));
} else if (fs.existsSync(path.join(packageCloudDir, "app", "api"))) {
  console.log("[vercel-prebuild] Using @decernhq/cloud package + route proxies...");
  ensureCloudApiProxies(path.join(packageCloudDir, "app", "api"));
} else {
  console.log(
    "[vercel-prebuild] No cloud layer found (local cloud/ or @decernhq/cloud package). Building OSS-only."
  );
}
